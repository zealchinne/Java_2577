class TypeCasting 
{
	public static void main(String[] args) 
	{
		int x=85;
		double d =(double)x;
		System.out.println(d); // 85.0
	}
}
public class Demo {
   public static void main(String[] args) {
      int a = 100;
      System.out.println("Integer: "+a);
      boolean bool = (a == 100);
      System.out.println("Converted to Bool: "+bool);
   }
}
